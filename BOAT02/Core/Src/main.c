/**
  * @brief 设置舵机角度
  * @param angle: 0 ~ 180 度
  */
void Servo_SetAngle(uint8_t angle) {
    // TIM2 配置：预分频 71，周期 19999 -> 50Hz，1μs 计数
    // 0.5ms = 500, 2.5ms = 2500
    uint32_t pulse = 500 + ((uint32_t)angle * 2000UL) / 180UL;
    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, pulse);
}
// 电调控制宏定义
#define THROTTLE_MIN 1000 // 1ms, 0%油门
#define THROTTLE_MAX 2000 // 2ms, 100%油门

/**
 * @brief 设置电机油门
 * @param throttle: 油门值，范围 [0, 1000]
 * @note  0 = 停转 (1ms), 1000 = 全速 (2ms)
 */
void Motor_SetThrottle(uint16_t throttle) {
    // 简单的范围限制
    if (throttle > 1000) throttle = 1000;
    // 将0-1000的油门值映射到1000-2000的脉宽值
    uint16_t pulse = THROTTLE_MIN + throttle;
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pulse);
}

/**
 * @brief 电调解锁/油门行程校准（推荐使用）
 * @note  这个函数会阻塞运行，并在校准完成后自动启动PWM。
 */
void ESC_Calibration(void) {
    // 1. 先输出一个"安全"信号，启动PWM
    Motor_SetThrottle(0);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
    HAL_Delay(2000);

    // 2. 发送最高油门信号
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, THROTTLE_MAX);
    HAL_Delay(2000);

    // 3. 发送最低油门信号
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, THROTTLE_MIN);
    HAL_Delay(2000);

    // 校准完成，PWM 持续输出最低油门
}
